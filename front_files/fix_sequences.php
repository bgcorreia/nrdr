      public function fixSigla() {

            $query = "select * from sequence s LEFT JOIN organism o ON s.IDOrganism = o.IDOrganism WHERE o.IDOrganism is NULL";
            $result = $this->banco->query($query);
            $array_sequence = $this->esp;
        
            while ($sequence = $result->fetch_array(MYSQLI_ASSOC)) {
                  $match = false;

                  foreach ($array_sequence as $sigla => $nome) {

                        $match = strpos($sequence['Header'], $sigla);
                        if($match){

                              /** Consulta se o organismo jÃ¡ esta cadastrado */
                              $query_organismo = "SELECT * FROM organism WHERE lower(Name) = '".strtolower($nome)."' limit 1";
                              $organism = $this->banco->query($query_organismo);
                              $org = $organism->fetch_array(MYSQLI_ASSOC);

                              if(!empty($org)){
                                    //Se jÃ¡ existir utiliza o existente
                                    $fix_query = "UPDATE sequence SET IDOrganism = {$org['IDOrganism']} WHERE IDSequence = {$sequence['IDSequence']}";
                                    $this->banco->query($fix_query);
                              }else{
                                    //Cadastra novo organismo
                                    $inset_org = "INSERT INTO organism (Name, CName, GenByCode) VALUES ('{$nome}', '', '1')";
                                    $this->banco->query($inset_org);
                                    $idOrg = $this->banco->insert_id;

                                    //Cadastra relacionamento organismo x banco
                                    $insert_dborg = "INSERT INTO dborganism (IDDB, IDOrganism, GenByCode) VALUES ('{$sequence['IDDB']}', '{$idOrg}', '1')";
                                    $this->banco->query($insert_dborg);

                                    //Faz update no organismo
                                    $fix_query = "UPDATE sequence SET IDOrganism = {$idOrg} WHERE IDSequence = {$sequence['IDSequence']}";
                                    $this->banco->query($fix_query);
                              }

                              continue 2;
                        }

                        if($nome === end($array_sequence)){
                              echo "\n\r Sequencia nÃ£o encontrada ID: {$sequence['IDSequence']} Header: {$sequence['Header']}";
                        }
                  }
            }
      }



